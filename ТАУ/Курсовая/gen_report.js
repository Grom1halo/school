function pts(pt) { return pt * 20; }  // пункты → twips (для fontSize halfPts используют pt*2 в docx)

function makeHeading(text, level, opts = {}) {
  const lvlMap = {
    1: HeadingLevel.HEADING_1,
    2: HeadingLevel.HEADING_2,
    3: HeadingLevel.HEADING_3,
  };
  return new Paragraph({
    text,
    heading: lvlMap[level] || HeadingLevel.HEADING_1,
    spacing: { before: 240, after: 120 },
    ...opts,
  });
}

function makePara(text, opts = {}) {
  return new Paragraph({
    children: [new TextRun({ text, size: 24, font: 'Times New Roman' })],
    spacing: { line: 360, before: 0, after: 120 },
    indent: { firstLine: convertMillimetersToTwip(12.5) },
    alignment: AlignmentType.JUSTIFIED,
    ...opts,
  });
}

function makeParaBold(text, opts = {}) {
  return new Paragraph({
    children: [new TextRun({ text, size: 24, bold: true, font: 'Times New Roman' })],
    spacing: { line: 360, before: 60, after: 60 },
    alignment: AlignmentType.JUSTIFIED,
    ...opts,
  });
}

function makeFormula(text, opts = {}) {
  return new Paragraph({
    children: [new TextRun({ text, size: 24, font: 'Courier New' })],
    spacing: { line: 300, before: 60, after: 60 },
    alignment: AlignmentType.CENTER,
    ...opts,
  });
}

function makeCaption(text) {
  return new Paragraph({
    children: [new TextRun({ text, size: 22, italics: true, font: 'Times New Roman' })],
    spacing: { before: 60, after: 180 },
    alignment: AlignmentType.CENTER,
  });
}

function makeImage(filename, widthMm = 150, heightMm = 100) {
  const fullPath = path.join(PLOTS_DIR, filename);
  if (!fs.existsSync(fullPath)) {
    console.warn(`  ПРЕДУПРЕЖДЕНИЕ: файл не найден: ${fullPath}`);
    return makePara(`[Рисунок: ${filename} не найден]`);
  }
  const imgData = fs.readFileSync(fullPath);
  const widthEmu  = Math.round(widthMm * 36000);   // 1 mm = 36000 EMU
  const heightEmu = Math.round(heightMm * 36000);
  return new Paragraph({
    children: [new ImageRun({
      data: imgData,
      transformation: { width: widthEmu / 9144, height: heightEmu / 9144 },
      // docx ImageRun принимает пиксели при 96 DPI; 1 mm = 3.7795 px
      // используем EMU напрямую через type
      type: 'png',
    })],
    alignment: AlignmentType.CENTER,
    spacing: { before: 120, after: 60 },
  });
}

